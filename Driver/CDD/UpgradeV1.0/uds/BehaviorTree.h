typedef enum
{
    Invalid,
    Success,
    Failure,
    Running,
    Aborted
} EStatus;

typedef enum
{
    Action,
    Condition,
    Sequence,
    Selector,
    Parallel
} EType;

typedef EStatus (*Tick)(void *object);

struct behavior
{
    /* data */
    EType   type;
    EStatus status;
    uint8 child_num;
    Tick Tick;
    void *object;
    struct behavior **child;
};

struct behaviorTree
{
    Tick Tick;
    void *object;
    struct behavior **root;
};

