/***********************************************************************
 * @brief       : Search Vconfig Index in Table
 * @param       : 
 * @retval      : 
 ***********************************************************************/
void AMP_VConfig(s_AMPVCfg_t *VCfg)
{
    uint16 Index;
    uint8 VCfgByte0 = 0u;
    uint8 VCfgByte27 = 0u;
    uint8 VCfgByte78 = 0u;
    uint8 VCfgByte23 = 0u;
    uint8 VCfgByte18 = 0u;
    uint8 VConfigCheck = 0u;
    uint8 VConfigMatched = 0u;
    Std_ReturnType ret;

    ret = VConfig_GetKindConfig(VCONFIG_KIND_INIT_FLAG,&VConfigCheck);
    ret |= VConfig_GetKindConfig(VCONFIG_KIND_PROJECT_CODE,&VCfgByte0);
    ret |= VConfig_GetKindConfig(VCONFIG_KIND_POWER_TRAIN_TYPE,&VCfgByte27);
    ret |= VConfig_GetKindConfig(VCONFIG_KIND_TRIM__LEVEL,&VCfgByte78);
    ret |= VConfig_GetKindConfig(VCONFIG_KIND_AMPLIFIER,&VCfgByte23);
    ret |= VConfig_GetKindConfig(VCONFIG_KIND_SPEAKER,&VCfgByte18);

    if( (TRUE == VConfigCheck) && (E_OK == ret) )
    {
		(void)amp_warn("VCfgBytes: 0x%x 0x%x 0x%x 0x%x 0x%x",VCfgByte0,VCfgByte27,VCfgByte78,VCfgByte23,VCfgByte18);

		for( Index = 0u; Index < VCFGNUM; Index++ )
		{
	   		if( (AMPVfgTable[Index].Byte0 == VCfgByte0) && (AMPVfgTable[Index].Byte27 == VCfgByte27) && 
				(AMPVfgTable[Index].Byte78 == VCfgByte78) )
	        {
				if( AMPVfgTable[Index].Byte23 == 0x07 )
				{
					if (AMPVfgTable[Index].byte18 == 0x1F )
					{
						VCfg->AMP1Cfg.AMPCfgSts = AMPVfgTable[Index].AMP1Cfg;
						VCfg->AMP2Cfg.AMPCfgSts = AMPVfgTable[Index].AMP2Cfg;
						VConfigMatched = 1u;
						break;
					}
					else if( AMPVfgTable[Index].byte18 == VCfgByte18 )
					{
						VCfg->AMP1Cfg.AMPCfgSts = AMPVfgTable[Index].AMP1Cfg;
						VCfg->AMP2Cfg.AMPCfgSts = AMPVfgTable[Index].AMP2Cfg;
						VConfigMatched = 1u;
						break;
					}
					else
					{
						/* do nothing */
					}
				}
				else if( AMPVfgTable[Index].Byte23 == VCfgByte23 )
				{
					if (AMPVfgTable[Index].byte18 == 0x1F )
					{
						VCfg->AMP1Cfg.AMPCfgSts = AMPVfgTable[Index].AMP1Cfg;
						VCfg->AMP2Cfg.AMPCfgSts = AMPVfgTable[Index].AMP2Cfg;
						VConfigMatched = 1u;
						break;
					}
					else if( AMPVfgTable[Index].byte18 == VCfgByte18 )
					{
						VCfg->AMP1Cfg.AMPCfgSts = AMPVfgTable[Index].AMP1Cfg;
						VCfg->AMP2Cfg.AMPCfgSts = AMPVfgTable[Index].AMP2Cfg;
						VConfigMatched = 1u;
						break;
					}
					else
					{
						/* do nothing */
					}
				}
				else
				{
					/* do nothing */
				}
	        }
	    }
		if( VConfigMatched == 0u )
		{
			(void)amp_info("VCfg Error Open All Channels");
			VCfg->AMP1Cfg.AMPCfgSts = 0x55u;
			VCfg->AMP2Cfg.AMPCfgSts = 0x55u;
		}
    }
    else
    {
        (void)amp_err("Vconfig Module not Config");
		(void)amp_info("VCfg Error Open All Channels");
		VCfg->AMP1Cfg.AMPCfgSts = 0x55u;
		VCfg->AMP2Cfg.AMPCfgSts = 0x55u;
    }
}

