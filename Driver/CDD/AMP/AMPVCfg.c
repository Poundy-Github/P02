    uint8 VCfgByte27 = 0u;
    uint8 VCfgByte78 = 0u;
    uint8 VCfgByte23 = 0u;
    uint8 VCfgByte18 = 0u;
    uint8 VConfigCheck = 0u;
    Std_ReturnType ret;
	s_AMPVCfg_t VCfg;

	/* default set all channel support */
	AMPChCfgSts = 0x00FFu;
	VCfg.AMP1Cfg.AMPCfgSts = 0x55u;
	VCfg.AMP2Cfg.AMPCfgSts = 0x55u;

    ret = VConfig_GetKindConfig(VCONFIG_KIND_INIT_FLAG,&VConfigCheck);
